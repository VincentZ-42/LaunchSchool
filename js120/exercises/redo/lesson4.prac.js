class Greeting {
  greet(message) {
    console.log(message);
  }
}

class Hello extends Greeting {
  hi() {
    super.greet('hello');
  }
}

class Goodbye extends Greeting {
  bye() {
    super.greet('Goodbye');
  }
}

let hi = new Hello();
let bye = new Goodbye();

hi.hi();
bye.bye();

function Person(name) {
  this.name = name;
  this.school = undefined;
}

Person.prototype.speak = function() {
  return `Hello, my name is ${this.name}.`;
};

